#include <stdio.h>

void main(void)
{
	int i = 10; 		printf("%d\n", i);
	short a = 10; 		printf("%hd\n", a);
	long b = 10;		printf("%ld\n", b);
	long long c = 10;	printf("%lld\n", c);

	unsigned int d = 10;	printf("%u\n", d);
	unsigned short e = 10;	printf("%hu\n", e);
	unsigned long l = 10;	printf("%lu\n", l);
	unsigned long long ll = 10; printf("%llu\n", ll);

	float f = 10.0;		printf("%f\n", f);
	double df = 10.0; 	printf("%lf\n", df);

	char x = 'a';	printf("%c\n", x);


}


